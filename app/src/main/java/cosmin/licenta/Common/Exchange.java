package cosmin.licenta.Common;

public class Exchange {
    private String type;
    private String rate;

    public Exchange(){

    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }
}
